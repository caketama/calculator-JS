function multiply() {
  let firstNum = document.getElementById("m1").value;
  let secondNum = document.getElementById("m2").value;
  document.getElementById("result").innerHTML = firstNum * secondNum;
}
function divide() {
  let firstNum = document.getElementById("d1").value;
  let secondNum = document.getElementById("d2").value;
  document.getElementById("result").innerHTML = firstNum * secondNum;
}
